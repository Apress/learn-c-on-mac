#include <stdio.h>

int main (int argc, const char * argv[]) {
	printf( "sizeof( char ) = %d\n", (int)sizeof( char ) );
	printf( "sizeof( short ) = %d\n", (int)sizeof( short ) );
	printf( "sizeof( int ) = %d\n", (int)sizeof( int ) );
	printf( "sizeof( long ) = %d\n", (int)sizeof( long ) );
	
	return 0;
}